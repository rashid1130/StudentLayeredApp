package com.nt.dto;

public class StudentDTO {
	private String name;
	private String course;
	private float  p1marks ;
	private float  p2marks;
	private float  p3marks;
	private float  p4marks;
	private float  p5marks;
	
	public StudentDTO() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCourse() {
		return course;
	}

	public void setCourse(String course) {
		this.course = course;
	}

	public float getP1marks() {
		return p1marks;
	}

	public void setP1marks(float p1marks) {
		this.p1marks = p1marks;
	}

	public float getP2marks() {
		return p2marks;
	}

	public void setP2marks(float p2marks) {
		this.p2marks = p2marks;
	}

	public float getP3marks() {
		return p3marks;
	}

	public void setP3marks(float p3marks) {
		this.p3marks = p3marks;
	}

	public float getP4marks() {
		return p4marks;
	}

	public void setP4marks(float p4marks) {
		this.p4marks = p4marks;
	}

	public float getP5marks() {
		return p5marks;
	}

	public void setP5marks(float p5marks) {
		this.p5marks = p5marks;
	}
	
	

}


